USE EFNI
GO
INSERT INTO EFNI.DBO.Persona
SELECT 
A.IdPersona,
NULL AS  [ULTIMA LLAMADA],
0 AS  [CCMENSUAL],
0 AS [CCGLOBAL],
0 AS [CCREPROCESO],
1 AS [DISPONIBLE],
0 AS [LOTE],
NULL AS [FECHA REPROCESO]
FROM GOLKIIDATA.DBO.PERSONA A
LEFT JOIN EFNI.DBO.PERSONA B ON A.IdPersona = B.EFNI_Persona
WHERE B.EFNI_Persona IS NULL
GO
INSERT INTO EFNI.dbo.Telefono
SELECT 
    Telefono,
    IdPersonas,
    NULL AS  [TIPIFICACION],
    NULL AS  [FECHA LLAMADA],
    0 AS [CC],
    0 AS [REPROCESADA],
    NULL AS  [FECHA REPROCESO],
    0 AS [LOTE],
    1 AS [DISPONIBLE]
FROM GOLKIIDATA.dbo.Telefonos A
LEFT JOIN EFNI.DBO.Telefono B ON A.Telefono = B.EFNI_Telefono
WHERE B.EFNI_Telefono IS NULL